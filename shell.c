char** parse_input(char* input) {
    char** args = malloc(MAX_ARGS * sizeof(char*));
    char* token;
    int i = 0;
    
    token = strtok(input, " \t\n");
    while (token != NULL && i < MAX_ARGS - 1) {
        args[i] = token;
        i++;
        token = strtok(NULL, " \t\n");
    }
    args[i] = NULL;
    
    return args;
}

